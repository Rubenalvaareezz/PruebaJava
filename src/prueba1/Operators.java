package prueba1;

public class Operators {
	public static void main(String[] args) {
		
		//Operators
		
		//Aritméticos
		
		var a = 5;
		var b = 2;
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a % b);
		
		//Lógico
		
		//AND
		System.out.println(true && true);
		//Or
		System.out.println(false || false);
		//Not
		System.out.println(!true);
		
		//Unarios
		
		System.out.println(a);
		System.out.println(-a);
		System.out.println(a++);

		
		
	
	}
}
